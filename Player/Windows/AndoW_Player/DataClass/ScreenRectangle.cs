
namespace HyOnPlayer
{
    public class ScreenRectangle
    {
        public float PosX = 0.0f;
        public float PosY = 0.0f;
        public float Width = 0.0f;
        public float Height = 0.0f;

        public ScreenRectangle(float tX, float tY, float tW, float tH)
        {
            PosX = tX;
            PosY = tY;
            Width = tW;
            Height = tH;
        }
    }
}
